package rungle;

/**
 * Created by Josh on 1/10/16.
 * Class that facilitates a pair object
 */
public class Pair {
    public char first;
    public char second;
    public Pair(char one, char two) {
        first = one;
        second = two;
    }
    public char getfirst() {
        return first;
    }
    public char getsecond() {
        return second;
    }
}
